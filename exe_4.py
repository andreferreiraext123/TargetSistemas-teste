# Sequência a
# A sequência é uma progressão aritmética com diferença de 2 entre os termos
lista_1 = [1, 3, 5, 7]
sequency_1 = lista_1[-1] + 2
print(f"O próximo elemento da lista é {sequency_1}")

# Sequência b
# A sequência é uma progressão geométrica com razão de 2
lista_2 = [2, 4, 8, 16, 32, 64]
sequency_2 = lista_2[-1] * 2
print(f"O próximo elemento da lista é {sequency_2}")

# Sequência c
# Cada número é o quadrado do número inteir
lista_3 = [0, 1, 4, 9, 16, 25, 36]
num = len(lista_3)
sequency_3 = num * num
print(f"O próximo elemento da sequência é {sequency_3}")

# Sequência d
# Cada número é o quadrado de numeros inteiros pares em ordem
lista_4 = [4, 16, 36, 64]
num = (len(lista_4) + 2) * 2
sequency_4 = num * num
print(f"O próximo elemento da sequência é {sequency_4}")

# Sequencia e
# Sequencia de fibonacci, onde cada número é a soma dos dois anteriores
lista_5 = [1, 1, 2, 3, 5, 8]
sequency_5 = lista_5[-2] + lista_5[-1]
print(f"O próximo elemento da lista é {sequency_5}")

# Sequência f
# A sequencia parece seguir um padrão de incremeto irregular
lista_6 = [2, 10, 12, 16, 17, 18, 19]
sequency_6 = lista_6[-1] + 1
print(f"O próximo elemento da lista é {sequency_6}")